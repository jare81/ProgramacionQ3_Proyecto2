    public void mostrarTwit() {
        area.setText("");
        
        Twits[] twits = user.obtenerTwitsActual();

        for (Twits twit : twits) {
            if (twit != null) {
                area.append(twit.toString() + "\n\n");
                  
            }
        }
        
        revalidate();
        repaint();
    }
    
